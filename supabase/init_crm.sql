create index idx_osoby_nastepny_kontakt on public.osoby(nastepny_kontakt);
create index idx_historia_kontaktu_osoba_id on public.historia_kontaktu(osoba_id);
create index idx_historia_kontaktu_data_kontaktu on public.historia_kontaktu(data_kontaktu desc);

create function public.sync_osoba_kontakt_dates()
returns trigger
language plpgsql
as $$
begin
  update public.osoby
     set ostatni_kontakt = greatest(coalesce(ostatni_kontakt, '-infinity'::timestamptz), new.data_kontaktu),
         nastepny_kontakt = coalesce(new.nastepny_kontakt, nastepny_kontakt)
   where id = new.osoba_id;

  return new;
end;
$$;

create trigger trg_sync_osoba_kontakt_dates
after insert on public.historia_kontaktu
for each row execute function public.sync_osoba_kontakt_dates();

