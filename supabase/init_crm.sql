create table public.historia_kontaktu (
  id bigint generated by default as identity primary key,
  osoba_id bigint not null,
  uzytkownik_id uuid,
  typ_kontaktu varchar(40) not null,
  opis text not null,
  data_kontaktu timestamptz not null default now(),
  nastepny_kontakt timestamptz,
  constraint historia_kontaktu_osoba_id_fkey
    foreign key (osoba_id)
    references public.osoby(id)
    on delete cascade,
  constraint historia_kontaktu_uzytkownik_id_fkey
    foreign key (uzytkownik_id)
    references public.uzytkownicy(id)
    on delete set null,
  constraint historia_kontaktu_typ_kontaktu_check
    check (typ_kontaktu in ('telefon', 'email', 'spotkanie', 'inne'))
);

